# Python tiene la particularidad de tener la posibilidad de instalar paquetes externos, que 
# dan más funcionalidades para trabajar con validaciones, planillas de cálculo, entre otros.

# Pypi es un repositorio muy utilizado en donde hay un sin fin de paquetes para poder instalar.

# Para instalar un paquete: pip install <modulo>
# Para actualizar un paquete: pip install <modulo> --upgrade

# Se puede buscar en tu navegador favorito, los paquetes según tu preferencia.

# Por ejemplo:
# 1.- 'Pandas', herramiento que es utilizada la Ciencia de Datos. Link: https://pypi.org/project/pandas/
# 2.- 'scikit-learn', herramienta que es utilizada en marchine learning (aprendizaje automático), por ejemplo para clasificar datos
#                     - Detección de spam, reconocimiento de imagenes. Link: https://pypi.org/project/scikit-learn/