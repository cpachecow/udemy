# Clases: Herramienta para crear objetos, que contienen característcas.
#         
# El nombre de una clase inicia con mayúscula y las otras letras con minúscula, por ejemplo class Pajaro:
# en caso que tenga dos o más palabras, cada una debe empezar por mayúscula, por ejemplo class HolaMundo:

class Pajaro:
    pass

# Instancia de la clase Pajaro(), significa que se pasa a una variable.
mi_pajaro = Pajaro()

# Es otra instancia de la misma clase, pero ubicada en otro espacio de memoria
otro_pajaro = Pajaro()

print(mi_pajaro)
print(otro_pajaro)